#entrada
temperatura_celsius = int(input('Digite a temperatura em celsius: '))
#processamento
temperatura_fahrenheit = (9 * temperatura_celsius + 160) / 5
#saída
print(f'A temperatura de {temperatura_celsius}ºC equivale a {temperatura_fahrenheit}ºF')