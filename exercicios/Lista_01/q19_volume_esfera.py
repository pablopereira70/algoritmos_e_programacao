#entrada
raio_esfera  = int(input('Digite o raio da esfera: '))
#processamento
volume_esfera = (4 * 3.14 * raio_esfera ** 3) / 3
#saída
print(f'A esfera de raio {raio_esfera} tem volume correspondente a {volume_esfera}')