#entrada
base_retangulo = int(input('Digite a base do retângulo:'))
altura_retangulo = int(input('Digite a altura do retângulo:'))
#processamento
area_retangulo = base_retangulo * altura_retangulo
#saída
print(f'O retângulo de base {base_retangulo} e altura {altura_retangulo} tem área igual a {area_retangulo}')